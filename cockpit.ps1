function Show-Menu {
    Clear-Host
    Write-Host ""
    Write-Host "??????????????????????????????????????????????????????????????????????????????" -ForegroundColor Cyan
    Write-Host "?                           GRACE - COCKPIT                                  ?" -ForegroundColor Cyan
    Write-Host "??????????????????????????????????????????????????????????????????????????????" -ForegroundColor Cyan
    Write-Host ""
    Write-Host "1. View Logs"
    Write-Host "2. Chat with Grace"
    Write-Host "3. System Status"
    Write-Host "4. Exit"
    Write-Host ""
}

while ($true) {
    Show-Menu
    $selection = Read-Host "Enter your choice"

    switch ($selection) {
        "1" {
            .\GRACE.ps1 -Logs
            Read-Host "Press Enter to continue..."
        }
        "2" {
            python .\chat_with_grace.py
            Read-Host "Press Enter to continue..."
        }
        "3" {
            .\GRACE.ps1 -Status
            Read-Host "Press Enter to continue..."
        }
        "4" {
            exit
        }
        default {
            Write-Host "Invalid selection" -ForegroundColor Red
            Read-Host "Press Enter to continue..."
        }
    }
}
